package pe.com.sedmail.core.parameters;

public class PeticionResponse {	
	
	private String idRuta;
	private String deRuta;
	private String caSpr;
	private String idPeticion;
	private String nomPeticion;
	private String idVehiculo;
	private String nuPlaca;
	
	public String getIdRuta() {
		return idRuta;
	}
	public void setIdRuta(String idRuta) {
		this.idRuta = idRuta;
	}
	public String getDeRuta() {
		return deRuta;
	}
	public void setDeRuta(String deRuta) {
		this.deRuta = deRuta;
	}
	public String getCaSpr() {
		return caSpr;
	}
	public void setCaSpr(String caSpr) {
		this.caSpr = caSpr;
	}
	public String getIdPeticion() {
		return idPeticion;
	}
	public void setIdPeticion(String idPeticion) {
		this.idPeticion = idPeticion;
	}
	public String getNomPeticion() {
		return nomPeticion;
	}
	public void setNomPeticion(String nomPeticion) {
		this.nomPeticion = nomPeticion;
	}
	public String getIdVehiculo() {
		return idVehiculo;
	}
	public void setIdVehiculo(String idVehiculo) {
		this.idVehiculo = idVehiculo;
	}
	public String getNuPlaca() {
		return nuPlaca;
	}
	public void setNuPlaca(String nuPlaca) {
		this.nuPlaca = nuPlaca;
	}
	
}
